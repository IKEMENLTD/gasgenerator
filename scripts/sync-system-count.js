#!/usr/bin/env node

/**
 * sync-system-count.js
 *
 * カタログページ (app/systems/catalog/page.tsx) の systems 配列から
 * システム数を算出し、以下を自動更新するスクリプト。
 *
 * 1. lib/generated/system-count.ts  — React/Next.js用の定数
 * 2. public/lp-page.html            — Render版LP（3箇所）
 * 3. netlify-tracking/frontend/index.html — Netlify版LP（3箇所）
 * 4. netlify-tracking/netlify/functions/admin-agencies.js — 代理店メール（5箇所）
 *
 * 実行: node scripts/sync-system-count.js
 */

const fs = require('fs')
const path = require('path')

const ROOT = path.resolve(__dirname, '..')

// ─── 1. カタログからシステム数をカウント ───

function countSystems() {
  const catalogPath = path.join(ROOT, 'app/systems/catalog/page.tsx')
  const content = fs.readFileSync(catalogPath, 'utf-8')

  // "const systems = [" の位置を特定
  const startMarker = 'const systems = ['
  const startIdx = content.indexOf(startMarker)
  if (startIdx === -1) {
    throw new Error('Could not find "const systems = [" in page.tsx')
  }

  // ブラケット深さカウントで対応する "]" を特定
  let depth = 0
  let arrayStart = startIdx + startMarker.length - 1 // "[" の位置
  let arrayEnd = -1

  for (let i = arrayStart; i < content.length; i++) {
    if (content[i] === '[') depth++
    if (content[i] === ']') depth--
    if (depth === 0) {
      arrayEnd = i
      break
    }
  }

  if (arrayEnd === -1) {
    throw new Error('Could not find matching "]" for systems array')
  }

  // 配列範囲内で id:\s*['"] をカウント
  const arrayContent = content.slice(arrayStart, arrayEnd + 1)
  const matches = arrayContent.match(/id:\s*['"]/g)
  const count = matches ? matches.length : 0

  if (count === 0) {
    throw new Error('No systems found in array — check the id pattern')
  }

  return count
}

// ─── 2. lib/generated/system-count.ts を生成 ───

function generateConstant(count) {
  const dir = path.join(ROOT, 'lib/generated')
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true })
  }

  const filePath = path.join(dir, 'system-count.ts')
  const content = `// Auto-generated by scripts/sync-system-count.js — do not edit
// Source: app/systems/catalog/page.tsx systems array
export const SYSTEM_COUNT = ${count}
`

  const existing = fs.existsSync(filePath) ? fs.readFileSync(filePath, 'utf-8') : ''
  if (existing === content) {
    console.log(`  lib/generated/system-count.ts — no change (${count})`)
    return false
  }

  fs.writeFileSync(filePath, content)
  console.log(`  lib/generated/system-count.ts — updated to ${count}`)
  return true
}

// ─── 3. 静的ファイルの数字を置換 ───

function replaceInFile(filePath, replacements) {
  const absPath = path.join(ROOT, filePath)
  if (!fs.existsSync(absPath)) {
    console.log(`  ${filePath} — file not found, skipping`)
    return false
  }

  let content = fs.readFileSync(absPath, 'utf-8')
  let changed = false

  for (const { pattern, replacer, label } of replacements) {
    const newContent = content.replace(pattern, replacer)
    if (newContent !== content) {
      content = newContent
      changed = true
      console.log(`  ${filePath} — ${label}`)
    }
  }

  if (changed) {
    fs.writeFileSync(absPath, content)
  } else {
    console.log(`  ${filePath} — no change`)
  }

  return changed
}

function updateStaticFiles(count) {
  let anyChanged = false

  // ── public/lp-page.html & netlify-tracking/frontend/index.html ──
  // 共通の3パターン
  const htmlReplacements = [
    {
      // パターン1: 「Nシステム公開中」（Catalogセクション）
      pattern: /(class="text-force"[^>]*>)\d+(<\/span>システム公開中)/,
      replacer: `$1${count}$2`,
      label: '「Nシステム公開中」updated',
    },
    {
      // パターン2: 「現在 N → 300+」（COMING_SOONセクション）
      // margin: 0 5px の直後の数字で、次の span が "→" を含む
      pattern: /(font-weight: 700; margin: 0 5px;">)\d+(<\/span>\s*<span[^>]*>→)/,
      replacer: `$1${count}$2`,
      label: '「現在 N → 300+」(COMING_SOON) updated',
    },
    {
      // パターン3: 「現在 N → 300+」（Urgencyセクション）
      // font-mono の直後の数字で、次が SVG 矢印
      pattern: /(font-family: var\(--font-mono\);">)\d+(<\/span>\s*<svg)/,
      replacer: `$1${count}$2`,
      label: '「現在 N → 300+」(Urgency) updated',
    },
  ]

  anyChanged |= replaceInFile('public/lp-page.html', htmlReplacements)
  anyChanged |= replaceInFile('netlify-tracking/frontend/index.html', htmlReplacements)

  // ── admin-agencies.js ──
  anyChanged |= replaceInFile(
    'netlify-tracking/netlify/functions/admin-agencies.js',
    [
      {
        pattern: /\d+種類の/g,
        replacer: `${count}種類の`,
        label: '「N種類の」updated (all occurrences)',
      },
    ]
  )

  return anyChanged
}

// ─── Main ───

function main() {
  console.log('Syncing system count...')

  const count = countSystems()
  console.log(`\nCatalog system count: ${count}\n`)

  let anyChanged = false
  anyChanged |= generateConstant(count)
  anyChanged |= updateStaticFiles(count)

  console.log(anyChanged ? '\nDone — files updated.' : '\nDone — everything already up to date.')
}

main()
